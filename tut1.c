#include <stdio.h>

int main()
{
    int a = 4;      //only integers
    float d = 3.22; //all real values
    char m = 'a';   //single characters

    printf("Hello World %d \n", a);
    printf("Hello World %f \n", d);
    printf("Hello World %c \n", m);
    return 0;
}

// %d is used for integers
//%f for real numbers
// %c for single characters